## O que é uma sala sensorial

Uma sala sensorial é um cômodo dedicado, com controle ambiental, projetado para ajudar pessoas com sensibilidades sensoriais a **se reequilibrar**. Não é um quarto de descanso comum: é um espaço com elementos específicos validados por especialistas em TEA.

## Os elementos típicos de uma sala sensorial

- **Iluminação ajustável** com dimmer e luzes coloridas suaves.
- **Isolamento acústico** ou som ambiente controlado.
- **Texturas variadas**: almofadas pesadas, cobertores ponderados, tapetes táteis.
- **Estímulos visuais opcionais**: tubos de bolhas, fibras óticas, projeções suaves.
- **Mobiliário macio e acolhedor**, sem quinas duras.
- **Privacidade**: porta fechada e ambiente silencioso quando necessário.

## Por que ela faz tanta diferença numa viagem

A viagem em si já é um desafio sensorial: aeroporto barulhento, voo longo, hotel desconhecido, comida diferente, multidão na piscina. A criança acumula estímulos e em algum momento precisa de um “reset”.

Sem sala sensorial, esse reset acontece no quarto — geralmente com choro, frustração e culpa dos pais. Com sala sensorial, é uma pausa rápida, planejada e até prazerosa.

Famílias que viajam com frequência relatam que a existência de sala sensorial no hotel é um dos fatores mais decisivos na escolha do destino. Algumas dizem que voltariam a um lugar simples só por causa disso.

## Como saber se a sala sensorial é “de verdade”

Cuidado com hotéis que chamam de “sala sensorial” qualquer cantinho silencioso. Os critérios mínimos para uma sala sensorial real são:

- **Espaço dedicado, com porta**, não só um canto da brinquedoteca.
- **Disponível durante todo o horário de funcionamento** do hotel.
- **Materiais validados por especialista em TEA**, não brinquedos comuns.
- **Manutenção e limpeza específicas** (alguns materiais exigem cuidado especial).

