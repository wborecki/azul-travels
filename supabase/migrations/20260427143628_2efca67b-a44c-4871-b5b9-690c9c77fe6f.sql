## Por que planejar mais do que o normal

Crianças autistas costumam funcionar melhor com previsibilidade. Quanto mais você antecipar o que vai acontecer (do que ela vai comer ao caminho até o quarto), menor a chance de uma crise. O planejamento extra não é exagero: é o que torna a viagem possível.

O bom planejamento se divide em três frentes: **escolher o lugar certo**, **preparar a criança** e **ter um plano B** para imprevistos.

## O que perguntar ao hotel antes de reservar

Não acredite só na descrição do site. Ligue ou mande mensagem fazendo perguntas específicas:

- A equipe foi treinada em TEA? Por qual instituição?
- Vocês têm sala sensorial ou um espaço calmo onde meu filho possa se reequilibrar?
- O quarto pode ser preparado antes da nossa chegada (luzes apagadas, cortinas fechadas, TV desligada)?
- Tem cardápio visual no restaurante? Vocês servem pratos simples como arroz branco, batata cozida e frango sem tempero?
- Como funciona o check-in? Posso fazer remotamente para evitar a fila e a recepção barulhenta?
- Tem fila prioritária no café da manhã ou em horário de pico?
- A piscina tem horário de menor movimento?

Se o hotel hesita ou desconversa nas respostas, é sinal de alerta. Procure outro.

## O que levar na mala além do essencial

Pense em conforto sensorial e regulação:

- **Fones abafadores** ou abafadores de ouvido descartáveis (essenciais em aeroporto e parques).
- **Objeto de conforto** que ela usa em casa: cobertor, brinquedo, almofada.
- **Rotina visual impressa** com fotos do hotel, do trajeto e das atividades.
- **Snacks preferidos** que talvez não estejam disponíveis no destino.
- **Tablet com filmes/jogos baixados** offline (não dependa do Wi-Fi).
- **Pijama do hotel não, do hotel** — leve o pijama de casa, é o cheiro familiar.
- **Medicação de uso contínuo** com receita atualizada e em quantidade extra.
- **Cartão de comunicação** de emergência com nome, condição e contatos.

## Como preparar a criança antes da viagem

A semana que antecede a viagem é decisiva:

- **Mostre fotos** do hotel, do quarto, da piscina, do restaurante. Quanto mais visual, melhor.
- **Faça role-play**: simule o check-in com bonecos, brinque de avião ou de fazer a mala.
- **Use uma rotina visual**: imprima um cronograma com pictogramas mostrando cada etapa do dia.
- **Conte uma “história social”**: um livrinho simples explicando “vamos viajar, vamos dormir num quarto novo, depois voltamos pra casa”.
- **Visite o local virtualmente** se houver tour 360°. O Turismo Azul vai oferecer isso para todos os destinos certificados.

## O que fazer se acontecer uma crise

Mesmo com tudo planejado, crises podem acontecer. Tenha um plano:

1. **Saia do estímulo**: leve a criança para um lugar mais silencioso e com menos luz.
2. **Use o objeto de conforto** e respeite o tempo dela. Não tente “explicar” — ela precisa se regular antes.
3. **Tenha um cartão pronto** para mostrar à equipe do hotel: “Meu filho é autista e está em crise. Por favor, dê espaço e silêncio.”
4. **Não se culpe**. Crise não é falha sua nem dela. Faz parte.
5. **Avise a equipe** depois de tudo passar. Hotéis preparados tratam isso com naturalidade e ainda agradecem o feedback.

