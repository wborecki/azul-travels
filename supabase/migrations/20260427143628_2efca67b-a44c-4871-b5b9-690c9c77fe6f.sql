- **Áreas de baixo estímulo**: pelo menos um ambiente sempre disponível com pouca gente e pouco ruído.

### 3. Adaptação de processos

- Check-in remoto, antecipado ou em horário alternativo.
- Cardápio visual com pictogramas e opção de pratos simples.
- Fila prioritária no café da manhã, piscina e áreas comuns.
- Concierge ou ponto focal TEA: alguém que recebe o perfil sensorial antes da chegada.
- Protocolo claro de manejo de crise (sem chamar segurança, sem expor a família).

### 4. Comunicação certa antes da chegada

- Receber e ler o **perfil sensorial** da criança com antecedência.
- Confirmar com a família o que pode ser preparado: tipo de luz, comida, horário de check-in.
- Designar a pessoa específica que vai recebê-los na chegada.

