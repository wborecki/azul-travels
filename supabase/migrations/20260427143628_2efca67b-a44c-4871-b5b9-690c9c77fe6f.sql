A legislação brasileira garante uma série de direitos às pessoas com Transtorno do Espectro Autista (TEA) — e muitos deles se aplicam diretamente ao turismo. Este guia reúne, em linguagem simples, o que você pode (e deve) exigir antes, durante e depois de uma viagem com seu filho autista.

## O que a Lei Berenice Piana garante

A Lei 12.764/2012, conhecida como Lei Berenice Piana, é o marco legal dos direitos das pessoas com TEA no Brasil. Ela equipara o autismo à condição de pessoa com deficiência para todos os efeitos legais. No contexto do turismo, isso significa três pilares fundamentais:

- **Atendimento prioritário** em estabelecimentos comerciais e prestadores de serviços, incluindo hotéis, restaurantes e parques.
- **Acesso a serviços com qualidade**, sem qualquer tipo de discriminação.
- **Proteção contra constrangimento ou recusa de atendimento** baseado na condição.

Estabelecimentos que descumprirem a lei estão sujeitos a multa, e o caso pode ser denunciado ao Procon, Ministério Público ou ouvidorias estaduais.

## Fila prioritária: quando e como exigir

A Lei 10.048/2000 garante atendimento prioritário a pessoas com deficiência em **bancos, repartições públicas, hotéis, restaurantes e qualquer estabelecimento de atendimento ao público**.

No turismo isso significa que seu filho autista tem direito a entrar antes das filas comuns em parques temáticos, museus, hotéis (check-in), atrações culturais e até em embarques de transporte. Para usar o benefício basta apresentar:

- A **Ciptea** (Carteira de Identificação da Pessoa com TEA), emitida gratuitamente pelo seu município ou estado.
- Ou o **laudo médico** com a indicação do CID F84 (Transtornos do Espectro Autista).

Dica prática: imprima ou tenha sempre uma foto da Ciptea e do laudo no celular. Em alguns estabelecimentos a equipe ainda não conhece a lei e o documento físico ajuda a resolver mais rápido.

## Entrada gratuita e meia-entrada

Vários estados e municípios brasileiros foram além da legislação federal. Hoje é comum encontrar:

- **Entrada gratuita** em zoológicos, jardins botânicos e museus municipais para a pessoa autista e até um acompanhante.
- **Meia-entrada** em cinemas, teatros, parques temáticos e eventos culturais.
- **Descontos especiais** em algumas redes hoteleiras parceiras de programas inclusivos.

Antes de viajar, vale a pena buscar “direitos pessoa autista [nome do estado]” no Google ou consultar a Secretaria de Cultura local. As regras variam, mas o esforço quase sempre compensa.

## O que muda com o PL 4108/2024

O Projeto de Lei 4108/2024 está em tramitação no Congresso e propõe a criação do **Selo Nacional de Turismo Inclusivo TEA**. Quando aprovado:

- Hotéis, restaurantes e atrações poderão obter um certificado federal padronizado.
- A capacitação da equipe em TEA passará a ser auditada por órgão público.
- A lista oficial de estabelecimentos certificados será divulgada nos canais do Ministério do Turismo.

Enquanto a lei não passa, o **Selo Azul**, emitido pela Absoluto Educacional, é hoje o critério mais rigoroso disponível no mercado: exige no mínimo 70% da equipe treinada em TEA/ABA com metodologia validada.

## O que fazer quando um estabelecimento não cumpre seus direitos

Infelizmente, ainda é comum receber respostas como “aqui não temos isso” ou “não sabíamos da lei”. Se isso acontecer:

